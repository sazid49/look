                    <div class="list-desc gray-clr">
                        <p>Der Creux du Van ist ein Kar (französisch cirque, zum Beispiel Cirque de Gavarnie) im Schweizer Jura an der Grenze zwischen den Kantonen Neuenburg und Waadt. Er ist etwa 1200 m breit und 500 Meter tief. Die Gesamtlänge der Felswände, die rund 160 Meter senkrecht abfallen, beträgt etwa vier Kilometer. Das Gebiet liegt beim Mont Soliat auf 1200 bis 1450 m ü. M. Seit 1972 ist es unter Schutz gestellt und gehört zum Bundesinventar der Landschaften und Naturdenkmäler von nationaler Bedeutung.</p>
                        <p>Der Kessel ist wahrscheinlich nach der Würm-Kaltzeit durch Erosion entstanden. Der Boden ist mit Moränen- und Bergsturzmaterial bedeckt, auf dem Tannen und Buchen wachsen. In der Mitte unter dem oben unbewaldeten Teil des Kessels liegt die Quelle Fontaine Froide, deren Wasser das ganze Jahr über gleichmässig 4 °C kalt ist.</p>
